package adalid.commons.util;

/**
 * @author Jorge Campins
 */
public class Damm {

    private static final int[][] QG = {
        {0, 3, 1, 7, 5, 9, 8, 6, 4, 2},
        {7, 0, 9, 2, 1, 5, 4, 8, 6, 3},
        {4, 2, 0, 6, 8, 7, 1, 3, 5, 9},
        {1, 7, 5, 0, 9, 8, 3, 4, 2, 6},
        {6, 1, 2, 3, 0, 4, 5, 9, 7, 8},
        {3, 6, 7, 4, 2, 0, 9, 5, 8, 1},
        {5, 8, 6, 9, 7, 2, 0, 1, 3, 4},
        {8, 9, 4, 5, 3, 6, 2, 0, 1, 7},
        {9, 4, 3, 8, 6, 1, 7, 2, 0, 5},
        {2, 5, 8, 1, 4, 3, 6, 7, 9, 0}};

    public static boolean check(String string) {
        int digit = digit(string);
        return digit == 0;
    }

    public static int digit(String string) {
        if (string == null) {
            return 0;
        }
        String num = string.trim();
        int col;
        int row = 0;
        for (int i = 0; i < num.length(); i++) {
            col = column(num.charAt(i));
            row = QG[row][col];
        }
        return row;
    }

    private static int column(char c) {
        try {
            return Integer.parseInt(String.valueOf(c));
        } catch (Exception e) {
            return 0;
        }
    }

}
